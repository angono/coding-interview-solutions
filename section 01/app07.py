# comparision operators
# equality
print(4==4)

# inequality
print(10!=4)

# greater than
print(4>4)

# less than
print(4<4)

# greater than or equal to
print(4>=4)

# less than or equal to
print(6<=4)


# logical operators
print(True and True)
print(True and False)
print(False or False)
print(not (False))








